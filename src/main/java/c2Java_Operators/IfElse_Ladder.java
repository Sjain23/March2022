package c2Java_Operators;

public class IfElse_Ladder {

	public static void main(String[] args) {
		
		if(1 >= 2) {
			System.out.println("this is correct");
		}
		else if(1 > 2) {
			System.out.println("this is correct");
		}
		else if(1 == 2) {
			System.out.println("this is correct");
		}
		else {
			System.out.println("you are a dumb programmer");
		}

	}

}
