package c2Java_Operators;

public class If_Statement {

	public static void main(String[] args) {
		
		//if statement
		
		if(2>3) {
			System.out.println("My condition is true");
		}
		
		if(2<3) {
			System.out.println("My condition is true");
		}
		
		int salary = 500;
		if(salary > 450) {
			System.out.println("My condition for salary is correct");
		}
		
		//if else statement:
		
		if(2<1) {
			System.out.println("My condition is true");
		} else {
			System.out.println("your logic is absurd");
		}
		
		int a = 23;
		if (a % 5 == 0) {
			System.out.println("this number 23 is divisible by 5");
		}
		else {
			System.out.println("this is not divisible by 5");
		}

	}

} 
